let a = 12, b = 6;
document.write("<b>Addition of " + a + " and " + b + " is <b>" + (a + b) + "<br><br>");  // Addition
document.write("<b>Subtraction of " + a + " and " + b + " is <b>" + (a - b) + "<br><br>"); // Subtraction
document.write("<b>Multiplication of " + a + " and " + b + " is <b>" + (a * b) + "<br><br>"); // Multiplication
